using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace KinectCoordinateMapping.Utilities
{
    public class Vector2
    {
        public float x;
        public float y;

        public Vector2()
        {
            x = 0;
            y = 0;
        }
        public Vector2(float xval, float yval)
        {
            x = xval;
            y = yval;
        }
    }
}
